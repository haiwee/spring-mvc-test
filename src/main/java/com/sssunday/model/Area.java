package com.sssunday.model;

public class Area {

	/**
	 * 区id
	 */
	private String id;
	
	/**
	 * 区名
	 */
	private String name;
	
	/**
	 * 所属市id
	 */
	private String pid;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPid() {
		return pid;
	}

	public void setPid(String pid) {
		this.pid = pid;
	}
	
}
